    private void btnCopiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCopiarActionPerformed
        Clipboard board = Toolkit.getDefaultToolkit().getSystemClipboard();
        ClipboardOwner selecao = new StringSelection(visor.getText());
        board.setContents((Transferable) selecao, selecao);
        btnCopiar.setEnabled(false);
    }//GEN-LAST:event_btnCopiarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        decimal.setText("");
        visor.setText("");
        btnCopiar.setEnabled(false);
        btnLimpar.setEnabled(false);
        btnConverter.setEnabled(false);
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        try {
            long dec;
            String hex;
            dec = Long.parseLong(decimal.getText());
            hex = Long.toHexString(dec).toUpperCase();
            visor.setText(hex);
            btnConverter.setEnabled(false);
            btnLimpar.setEnabled(true);
            btnCopiar.setEnabled(true);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ERRO! \n" + ex);
            decimal.setText("");
            visor.setText("");
            btnCopiar.setEnabled(false);
            btnLimpar.setEnabled(false);
            btnConverter.setEnabled(false);
        }
    }//GEN-LAST:event_btnConverterActionPerformed


